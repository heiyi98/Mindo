// 根据country_code和省级代码查行政时区
export function getAdministrativeTimezone(
  countryCode: string,
  stateCode?: string
): string {
  const entry = COUNTRY_TIMEZONES[countryCode.toLowerCase()];
  if (!entry) return 'UTC';

  // 单时区国家
  if (typeof entry === 'string') return entry;

  // 多时区国家，用省级代码细分
  if (stateCode && entry[stateCode]) {
    return entry[stateCode] as string;
  }

  // fallback到默认时区
  return (entry['default'] as string) || 'UTC';
}
